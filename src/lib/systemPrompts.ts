export type PersonaKey = keyof typeof personaPrompts;
export type PlatformKey = keyof typeof platformPrompts;

/**
 * 최종 시스템 프롬프트 조합: 페르소나 + 플랫폼별 형식
 */
export function buildSystemPrompt(persona: PersonaKey, platform: PlatformKey): string {
  return `${personaPrompts[persona]}\n\n${platformPrompts[platform]}`;
}
